"""–û–±—â–∏–µ –∫–ª–∞–≤–∏–∞—Ç—É—Ä—ã –¥–ª—è –±–æ—Ç–∞.

–ö–ª–∞–≤–∏–∞—Ç—É—Ä–∞ - —ç—Ç–æ –∫–Ω–æ–ø–∫–∏, –∫–æ—Ç–æ—Ä—ã–µ –ø–æ—è–≤–ª—è—é—Ç—Å—è –≤–º–µ—Å—Ç–æ –æ–±—ã—á–Ω–æ–π –∫–ª–∞–≤–∏–∞—Ç—É—Ä—ã.
–≠—Ç–æ –∫–∞–∫ –º–µ–Ω—é –≤ —Ä–µ—Å—Ç–æ—Ä–∞–Ω–µ: —Ç—ã –≤–∏–¥–∏—à—å –∫–Ω–æ–ø–∫–∏ –∏ –º–æ–∂–µ—à—å –≤—ã–±—Ä–∞—Ç—å, —á—Ç–æ —Ö–æ—á–µ—à—å.
"""
from aiogram.types import ReplyKeyboardMarkup, KeyboardButton
from aiogram.utils.keyboard import ReplyKeyboardBuilder


def get_main_menu() -> ReplyKeyboardMarkup:
    """–°–æ–∑–¥–∞—ë—Ç –≥–ª–∞–≤–Ω–æ–µ –º–µ–Ω—é –±–æ—Ç–∞ —Å –∫–Ω–æ–ø–∫–∞–º–∏.
    
    –≠—Ç–æ –∫–∞–∫ –º–µ–Ω—é –≤ —Ä–µ—Å—Ç–æ—Ä–∞–Ω–µ: –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –≤–∏–¥–∏—Ç –∫–Ω–æ–ø–∫–∏ –∏ –º–æ–∂–µ—Ç –≤—ã–±—Ä–∞—Ç—å,
    —á—Ç–æ –æ–Ω —Ö–æ—á–µ—Ç —Å–¥–µ–ª–∞—Ç—å.
    
    Returns:
        ReplyKeyboardMarkup: –ö–ª–∞–≤–∏–∞—Ç—É—Ä–∞ —Å –∫–Ω–æ–ø–∫–∞–º–∏ –º–µ–Ω—é
    """
    # –°–æ–∑–¥–∞—ë–º —Å—Ç—Ä–æ–∏—Ç–µ–ª—å –∫–ª–∞–≤–∏–∞—Ç—É—Ä—ã (—ç—Ç–æ –∫–∞–∫ –∫–æ–Ω—Å—Ç—Ä—É–∫—Ç–æ—Ä –¥–ª—è –∫–Ω–æ–ø–æ–∫)
    builder = ReplyKeyboardBuilder()
    
    # –î–æ–±–∞–≤–ª—è–µ–º –∫–Ω–æ–ø–∫–∏ –≤ –∫–ª–∞–≤–∏–∞—Ç—É—Ä—É
    # –ö–∞–∂–¥–∞—è –∫–Ω–æ–ø–∫–∞ - —ç—Ç–æ –¥–µ–π—Å—Ç–≤–∏–µ, –∫–æ—Ç–æ—Ä–æ–µ –º–æ–∂–µ—Ç –≤—ã–ø–æ–ª–Ω–∏—Ç—å –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å
    builder.add(KeyboardButton(text="üìù –≠—Ö–æ"))
    builder.add(KeyboardButton(text="ü§ñ ChatGPT"))
    builder.add(KeyboardButton(text="‚ÑπÔ∏è –ü–æ–º–æ—â—å"))
    builder.add(KeyboardButton(text="üîÑ –û–±–Ω–æ–≤–∏—Ç—å –º–µ–Ω—é"))
    
    # –ù–∞—Å—Ç—Ä–∞–∏–≤–∞–µ–º —Ä–∞—Å–ø–æ–ª–æ–∂–µ–Ω–∏–µ –∫–Ω–æ–ø–æ–∫
    # adjust(1, 1, 1, 1) –æ–∑–Ω–∞—á–∞–µ—Ç: –ø–æ –æ–¥–Ω–æ–π –∫–Ω–æ–ø–∫–µ –≤ –∫–∞–∂–¥–æ–π —Å—Ç—Ä–æ–∫–µ
    # –≠—Ç–æ –∫–∞–∫ —Ä–∞—Å—Å—Ç–∞–≤–∏—Ç—å —Ç–∞—Ä–µ–ª–∫–∏ –Ω–∞ —Å—Ç–æ–ª–µ - –ø–æ –æ–¥–Ω–æ–π –≤ —Ä—è–¥
    builder.adjust(1, 1, 1, 1)
    
    # –î–µ–ª–∞–µ–º –∫–ª–∞–≤–∏–∞—Ç—É—Ä—É –ø–æ—Å—Ç–æ—è–Ω–Ω–æ–π (–Ω–µ –∏—Å—á–µ–∑–∞–µ—Ç –ø–æ—Å–ª–µ –Ω–∞–∂–∞—Ç–∏—è)
    # resize_keyboard=True –æ–∑–Ω–∞—á–∞–µ—Ç, —á—Ç–æ –∫–ª–∞–≤–∏–∞—Ç—É—Ä–∞ –ø–æ–¥—Å—Ç—Ä–æ–∏—Ç—Å—è –ø–æ–¥ —Ä–∞–∑–º–µ—Ä —ç–∫—Ä–∞–Ω–∞
    return builder.as_markup(resize_keyboard=True)


