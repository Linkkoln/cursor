    """–û–±—Ä–∞–±–æ—Ç—á–∏–∫ –∫–Ω–æ–ø–∫–∏ "–ü–æ–º–æ—â—å" –≤ –º–µ–Ω—é.
    
    –ö–æ–≥–¥–∞ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –Ω–∞–∂–∏–º–∞–µ—Ç –∫–Ω–æ–ø–∫—É "–ü–æ–º–æ—â—å",
    –≤—ã–∑—ã–≤–∞–µ—Ç—Å—è —Ç–∞ –∂–µ —Ñ—É–Ω–∫—Ü–∏—è, —á—Ç–æ –∏ –¥–ª—è –∫–æ–º–∞–Ω–¥—ã /help.
    """
    # –í—ã–∑—ã–≤–∞–µ–º —Ç—É –∂–µ —Ñ—É–Ω–∫—Ü–∏—é, —á—Ç–æ –∏ –¥–ª—è –∫–æ–º–∞–Ω–¥—ã /help
    await cmd_help(message)
